    private void tblSheetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSheetMouseClicked
        //selectPrice = (boolean) tblPrices.getValueAt(tblPrices.getSelectedRow(), 0);
        sheetId = (int) tblSheet.getValueAt(tblSheet.getSelectedRow(), 1);
        String wagonId = (String) tblSheet.getValueAt(tblSheet.getSelectedRow(), 2);
        String classType = (String) tblSheet.getValueAt(tblSheet.getSelectedRow(), 3);
        status = (String) tblSheet.getValueAt(tblSheet.getSelectedRow(), 4);
        txtWagonId.setText(wagonId);
        if (classType.equals("First")) {
            btnFist.setSelected(true);
        }

        if (classType.equals("Second")) {
            btnSecond.setSelected(true);
        }
        if (classType.equals("Third")) {
            btnThird.setSelected(true);
        }
        if (classType.equals("Observation")) {
            btnObservation.setSelected(true);
        }

    }//GEN-LAST:event_tblSheetMouseClicked

