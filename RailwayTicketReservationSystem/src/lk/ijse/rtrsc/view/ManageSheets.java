    private void getAllSheets() {
        try {
            List<SheetDto> sheetDto = ManageSheetController.getAllSheets();
            dtm.setRowCount(0);

            if (sheetDto != null) {

                for (SheetDto sheet : sheetDto) {
                    Object[] ob = {false, sheet.getSheetID(), sheet.getWagonDto().getWagonId(), sheet.getSheetType(), sheet.getStatus()};
                    dtm.addRow(ob);
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageTrain.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    @Override
    public void updateObservers() throws Exception {
        getAllSheets();
    }

