    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String wagonId = txtWagonId.getText();
            WagonDto wagon = new WagonDto();

            WagonDto searchWagon = ManageWagonController.searchWagons(wagonId);
            wagon.setWagonId(wagonId);
            wagon.setClassType(searchWagon.getClassType());
            wagon.setSheetQuantity(searchWagon.getSheetQuantity());
            String classType = null;
            String status = "Unbooked";
            if (btnFist.isSelected()) {
                classType = "First";
            }
            if (btnSecond.isSelected()) {
                classType = "Second";
            }
            if (btnThird.isSelected()) {
                classType = "Third";
            }
            if (btnObservation.isSelected()) {
                classType = "Observation";
            }
            SheetDto dto = new SheetDto();
            dto.setWagonDto(wagon);
            dto.setSheetType(classType);
            dto.setStatus(status);
            boolean isAdded = ManageSheetController.saveSheet(dto);
            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Added Successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Added Fail");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageSheets.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

