    private void loadKeyListener() {
        KeyboardFocusManager manager = KeyboardFocusManager.getCurrentKeyboardFocusManager();
        manager.addKeyEventPostProcessor(new KeyEventPostProcessor() {

            @Override
            public boolean postProcessKeyEvent(KeyEvent e) {
                if (e.getID() == KeyEvent.KEY_PRESSED) {
                    switch (e.getKeyCode()) {
                        case KeyEvent.VK_F1:
                            DashBordPanel();
                            break;
                        case KeyEvent.VK_F2:
                            ManageTrainPanel();
                            break;
                        case KeyEvent.VK_F3:
                            ManageWagonsPanel();
                            break;
                        case KeyEvent.VK_F4:
                             ManagePrices();
                             break;
                        case KeyEvent.VK_F5:
                             ManageRoutePanel();
                             break;                          
                        case KeyEvent.VK_F6:
                            ManageStationsPanel();
                            break;
                        case KeyEvent.VK_F7:
                            userPanel();
                            break;
                    }
                }
                return true;
            }

        });
    }

